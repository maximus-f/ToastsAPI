package me.perotin;

/**
 * Represents the style of a toast notification.
 */
public enum ToastStyle {
    /**
     * Represents a goal toast style.
     */
    GOAL,
    
    /**
     * Represents a task toast style.
     */
    TASK,
    
    /**
     * Represents a challenge toast style.
     */
    CHALLENGE
}